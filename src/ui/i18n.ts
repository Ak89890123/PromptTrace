export function roleLabel(role: AssetRole, language: ResolvedLanguage): string {
  return ROLE_TEXT[language][role];
}

export function assetTypeLabel(type: AssetType, language: ResolvedLanguage): string {
  return ASSET_TYPE_TEXT[language][type];
}

export function categoryLabel(category: { id: string; name: string; isBuiltin?: boolean }, language: ResolvedLanguage): string {
  return category.isBuiltin ? BUILTIN_CATEGORY_TEXT[language][category.id] ?? category.name : category.name;
}

